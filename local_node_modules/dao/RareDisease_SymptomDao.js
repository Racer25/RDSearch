var ConnectionProvider = require("../dao/ConnectionProvider.js");
var mysql = require('mysql');

var getRareDisease_Symptom = function(orphanetID, idSymptom, callback)
{
    var connection=ConnectionProvider.getConnection();

    var sql = "SELECT * FROM provok WHERE idSymptom=? AND orphanetID=?";
    var inserts = [idSymptom, orphanetID];
    sql = mysql.format(sql, inserts);

    connection.query(sql, function (error, results, fields) {
        if (error) throw error;
        callback(results[0]);
    });
};

