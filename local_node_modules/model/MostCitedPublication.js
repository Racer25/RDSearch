var MostCitedPublication = {
    // Init the publication
    init: function (id, title, abstract, authors, doi, lastUpdateDate, url)
    {
        this.id = id;
        this.title = title;
        this.abstract = abstract;
        this.authors = authors;
        this.doi=doi;
        this.lastUpdateDate=lastUpdateDate;
        this.url=url;
    },

    // Give description of th publication
    print: function () {
        console.log("Publication");
        console.log("Id: "+this.id);
        console.log("Title: "+this.title);
        console.log("Authors: "+this.authors);
        console.log("DOI: "+ this.doi);
        console.log("LastUpdateDate: "+ this.lastUpdateDate);
    }
};

exports.init = MostCitedPublication.init;
exports.print = MostCitedPublication.print;
